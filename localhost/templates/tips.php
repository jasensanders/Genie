<h1> Tips!</h1>
<p><strong> Be Specific!  The more specific you can be with your seed word or phrase the closer you will get to something useful.</strong></p>
<p><strong> Remember your parts of speech!</strong></p>
<p><strong> Noun: person,place or thing.</strong></p>
<p><strong> Verb: an action, state or occurance.</strong></p>
<p><strong> Adjective: Noun modifier. could be a noun, verb, adverb.</strong></p>
<p><strong> Adverb: verb modifier. could be a noun, another verb or an adjective.</strong></p>
<p><strong> Use the search engine that is more suited to what you expect to find.  Amazon will find you things, where google and bing can find you concepts. Pinterest on the other hand can find you instructions.</strong></p>
